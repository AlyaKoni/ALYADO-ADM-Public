function Main
{
    Write-Host "Getting all guest accounts" -ForegroundColor $CommandInfo
    $Users = Get-MsolUser -All | where { $_.UserType -eq "Guest" }

    Write-Host "Checking $($Users.Count) accounts"
    foreach($User in $Users)
    {
        #$user = $users[20]
        Write-Host "=============================== $($user.AlternateEmailAddresses)"
        $oldUser = @{disp=$User.DisplayName;first=$User.FirstName;last=$User.LastName}
        Write-Host "OLD: $($oldUser | ConvertTo-Json -Compress)"
        #$newUser | ConvertTo-Json

        if ($user.UserPrincipalName.Contains("#"))
        {
            $email = $user.UserPrincipalName.Split("#")[0]
            $name = $email.Substring(0, $email.LastIndexOf("_"))
            $domain = $email.Substring($email.LastIndexOf("_")+1)
            $domainParts = $domain.Split(".")
            $comp = Make-PascalCase($domainParts[$domainParts.Length-2])
            if ($domainParts.Length -gt 2)
            {
                $comp += " " + (Make-PascalCase($domainParts[$domainParts.Length-3]))
            }
        }
        else
        {
            $name = $user.UserPrincipalName.Split("@")[0]
            $domain = $user.UserPrincipalName.Split("@")[1]
            $domainParts = $domain.Split(".")
            $comp = Make-PascalCase($domainParts[$domainParts.Length-2])
            if ($domainParts.Length -gt 2)
            {
                $comp += " " + (Make-PascalCase($domainParts[$domainParts.Length-3]))
            }
        }
        if ($comp -eq "Outlook" -or `            $comp -eq "Gmx" -or `            $comp -eq "Hotmail" -or `            $comp -eq "Hispeed" -or `            $comp -eq "Gmail" -or `            $comp -eq "Bluewin" -or `            $comp -eq "Bluemail")
        {
            #$comp = "Extern"
            $comp = $domain.ToLower()
        }
        if ($oldUser.first) {$first = $oldUser.first.ToLower()} else {$first = ""}
        if ($oldUser.last) {$last = $oldUser.last.ToLower()} else {$last = ""}
        if ($oldUser.disp) {$disp = $oldUser.disp.ToLower()} else {$disp = ""}
        $first = $first.Replace("ä","ae").Replace("ü","ue").Replace("ö","oe").Replace("é","e").Replace("è","e").Replace("à","a")
        $last = $last.Replace("ä","ae").Replace("ü","ue").Replace("ö","oe").Replace("é","e").Replace("è","e").Replace("à","a")
        $disp = $disp.Replace("ä","ae").Replace("ü","ue").Replace("ö","oe").Replace("é","e").Replace("è","e").Replace("à","a")

        $namePrts = $name.Split(".-_")
        switch($namePrts.Length)
        {
            1 {
                if ($namePrts[0] -ne ($first+$last) -and $namePrts[0] -ne ($last+$first))
                {
                    if ($vornamen -contains $namePrts[0])
                    {
                        $first = $namePrts[0]
                    }
                    else
                    {
                        if (-Not ($first -eq $namePrts[0] -and $last -eq "" -or `                            $last -eq $namePrts[0] -and $first -eq "" -or `                            $last -ne "" -and $first -ne ""))
                        {
                            $decision = $Host.UI.PromptForChoice("Confirm", "What is '$($namePrts[0])'?", @("&First", "&Last", "&None", "&Skip"), 1)
                            if ($decision -eq 0)
                            {
                                $first = $namePrts[0]
                                $last = ""
                            }
                            if ($decision -eq 1)
                            {
                                $last = $namePrts[0]
                                $first = ""
                            }
                            if ($decision -eq 2)
                            {
                                $response  = $Host.UI.Prompt("Question", "Specify first and lst for: '$($namePrts[0])'?", @("First", "Last"))
                                $first = $response["First"]
                                $last = $response["Last"]
                            }
                        }
                    }
                }
            }
            {($_ -gt 1)} {
                switch($namePrts.Length)
                {
                    2 {
                        $tests = @(@{
                            "first" = $namePrts[0]
                            "last" = $namePrts[1]
                        },@{
                            "last" = $namePrts[0]
                            "first" = $namePrts[1]
                        })
                    }
                    3 {
                        $tests = @(@{
                            "first" = $namePrts[0]+"-"+$namePrts[1]
                            "last" = $namePrts[2]
                        },@{
                            "first" = $namePrts[0]
                            "last" = $namePrts[1]+"-"+$namePrts[2]
                        },@{
                            "first" = $namePrts[0]+" "+$namePrts[1]
                            "last" = $namePrts[2]
                        },@{
                            "first" = $namePrts[0]
                            "last" = $namePrts[1]+" "+$namePrts[2]
                        },
                        @{
                            "last" = $namePrts[0]+"-"+$namePrts[1]
                            "first" = $namePrts[2]
                        },@{
                            "last" = $namePrts[0]
                            "first" = $namePrts[1]+"-"+$namePrts[2]
                        },@{
                            "last" = $namePrts[0]+" "+$namePrts[1]
                            "first" = $namePrts[2]
                        },@{
                            "last" = $namePrts[0]
                            "first" = $namePrts[1]+" "+$namePrts[2]
                        })
                    }
                }
                $fndVorname = $null
                foreach($test in $tests)
                {
                    if ($vornamen -contains $test.first)
                    {
                        $first = $test.first
                        $last = $test.last.Replace("-"," ")
                        $fndVorname = $test.first
                        break
                    }
                }
                if (-Not $fndVorname)
                {
                    switch($namePrts.Length)
                    {
                        2 {
                            if (($first -ne $namePrts[0] -and $last -ne $namePrts[1]) -and `
                                ($first -ne $namePrts[1] -and $last -ne $namePrts[0]))
                            {
                                $first = $namePrts[0]
                                $last = $namePrts[1]
                                $decision = $Host.UI.PromptForChoice("Confirm", "Is First='$($first)' Last='$($last)' correct?", @("&Yes", "&Swap"), 0)
                                if ($decision -eq 1)
                                {
                                    $tmp = $first
                                    $first = $last
                                    $last = $tmp
                                }
                            }
                        }
                        3 {
                            if (($first -ne $namePrts[0] -and $last -ne ($namePrts[1]+" "+$namePrts[2])) -and `
                                ($first -ne ($namePrts[0]+"-"+$namePrts[1]) -and $last -ne $namePrts[2]) -and `
                                ($first -ne ($namePrts[0]+" "+$namePrts[1]) -and $last -ne $namePrts[2]) -and `
                                ($last -ne $namePrts[0] -and $first -ne ($namePrts[1]+"-"+$namePrts[2])) -and `
                                ($last -ne $namePrts[0] -and $first -ne ($namePrts[1]+" "+$namePrts[2])) -and `
                                ($last -ne ($namePrts[0]+" "+$namePrts[1]) -and $first -ne $namePrts[2]))
                            {
                                $first = $namePrts[0]
                                $last = ($namePrts[1]+" "+$namePrts[2])
                                $decision = $Host.UI.PromptForChoice("Confirm", "Is First='$($first)' Last='$($last)' correct?", @("&Yes", "&No"), 0)
                                if ($decision -eq 1)
                                {
                                    $first = ($namePrts[0]+" "+$namePrts[1])
                                    $last = $namePrts[2]
                                    $decision = $Host.UI.PromptForChoice("Confirm", "Is First='$($first)' Last='$($last)' correct?", @("&Yes", "&No"), 0)
                                    if ($decision -eq 1)
                                    {
                                        $first = $namePrts[2]
                                        $last = ($namePrts[0]+" "+$namePrts[1])
                                        $decision = $Host.UI.PromptForChoice("Confirm", "Is First='$($first)' Last='$($last)' correct?", @("&Yes", "&No"), 0)
                                        if ($decision -eq 1)
                                        {
                                            $first = ($namePrts[1]+" "+$namePrts[2])
                                            $last = $namePrts[0]
                                            $decision = $Host.UI.PromptForChoice("Confirm", "Is First='$($first)' Last='$($last)' correct?", @("&Yes", "&No"), 0)
                                            if ($decision -eq 1)
                                            {
                                                $first = ($namePrts[0]+"-"+$namePrts[1])
                                                $last = $namePrts[2]
                                                $decision = $Host.UI.PromptForChoice("Confirm", "Is First='$($first)' Last='$($last)' correct?", @("&Yes", "&No"), 0)
                                                if ($decision -eq 1)
                                                {
                                                    $first = ($namePrts[1]+"-"+$namePrts[2])
                                                    $last = $namePrts[0]
                                                    $decision = $Host.UI.PromptForChoice("Confirm", "Is First='$($first)' Last='$($last)' correct?", @("&Yes", "&No"), 0)
                                                    if ($decision -eq 1)
                                                    {
                                                        throw "really?"
                                                    }
                                                }
                                            }
                                        }
                                    }
                                }
                            }
                        }
                    }
                }
                else
                {
                    if ($namePrts.Length -gt 2)
                    {
                        if ($namePrts[0] -eq $fndVorname)
                        {
                            if ($vornamen -contains $namePrts[1])
                            {
                                $first = $namePrts[0]+" "+$namePrts[1]
                                $last = $namePrts[2]
                            }
                            else
                            {
                                $first = $namePrts[0]
                                $last = $namePrts[1]+" "+$namePrts[2]
                            }
                        }
                        if ($namePrts[2] -eq $fndVorname)
                        {
                            $first = $namePrts[2]
                            $last = $namePrts[0]+" "+$namePrts[1]
                        }
                    }
                }
            }
            default { throw "Not implemented" }
        }
        $first = Make-PascalCase -string $first
        $last = Make-PascalCase -string $last
        $firstChk = $first.Replace("ae","ä").Replace("ue","ü").Replace("oe","ö")
        if ($disp.Contains($firstChk))
        {
            $first = $firstChk
        }
        $lastChk = $last.Replace("ae","ä").Replace("ue","ü").Replace("oe","ö")
        if ($disp.Contains($lastChk))
        {
            $last = $lastChk
        }
        $disp = ($first + " " + $last).Trim() + " " + $CompStart + $comp + $CompEnd
        if ($first -eq "") {$first = $null}
        if ($last -eq "") {$last = $null}
        $newUser = @{disp=$disp;first=$first;last=$last}
        if ($oldUser.first) {$first = $oldUser.first.ToLower()} else {$first = ""}
        if ($oldUser.last) {$last = $oldUser.last.ToLower()} else {$last = ""}
        if ($oldUser.disp) {$disp = $oldUser.disp.ToLower()} else {$disp = ""}
        $first = $first.Replace("ä","ae").Replace("ü","ue").Replace("ö","oe").Replace("é","e").Replace("è","e").Replace("à","a")
        $last = $last.Replace("ä","ae").Replace("ü","ue").Replace("ö","oe").Replace("é","e").Replace("è","e").Replace("à","a")
        $disp = $disp.Replace("ä","ae").Replace("ü","ue").Replace("ö","oe").Replace("é","e").Replace("è","e").Replace("à","a")

        if (($oldUser | ConvertTo-Json -Compress) -ne ($newUser | ConvertTo-Json -Compress))
        {
            Write-Host "NEW: $($newUser | ConvertTo-Json -Compress)"
        
            $decision = $Host.UI.PromptForChoice("Confirm", "Update?", @("&Yes", "&No", "&Stop"), 1)
            if ($decision -eq 2)
            {
                Return
            }
            if ($decision -eq 0)
            {
                Set-MsolUser -ObjectId $user.ObjectId -FirstName $newUser.first -LastName $newUser.last -DisplayName $newUser.disp
            }
        }
    }
}

