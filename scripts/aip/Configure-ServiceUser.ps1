[CmdletBinding()]
Param(
)

#Reading configuration
. $PSScriptRoot\..\..\01_ConfigureEnv.ps1

#Starting Transscript
Start-Transcript -Path "$($AlyaLogs)\scripts\aip\Configure-ServiceUser-$($AlyaTimeString).log" | Out-Null

# Constants
$KeyVaultName = "$($AlyaNamingPrefix)keyv$($AlyaResIdMainKeyVault)"
$KeyVaultResourceGroupName = "$($AlyaNamingPrefix)resg$($AlyaResIdMainInfra)"

# Checking modules
Write-Host "Checking modules" -ForegroundColor $CommandInfo
Install-ModuleIfNotInstalled "Az.Accounts"
Install-ModuleIfNotInstalled "Az.Resources"
Install-ModuleIfNotInstalled "Az.KeyVault"
Install-ModuleIfNotInstalled "Microsoft.Graph.Authentication"
Install-ModuleIfNotInstalled "Microsoft.Graph.Beta.Identity.Governance"

# Logging in
Write-Host "Logging in" -ForegroundColor $CommandInfo
LoginTo-Az -SubscriptionName $AlyaSubscriptionName
LoginTo-MgGraph -Scopes "Directory.ReadWrite.All","RoleManagement.Read.All","RoleManagement.ReadWrite.Directory"

# =============================================================
# O365 stuff
# =============================================================

Write-Host "`n`n=====================================================" -ForegroundColor $CommandInfo
Write-Host "Aip | Configure-ServiceUser | O365" -ForegroundColor $CommandInfo
Write-Host "=====================================================`n" -ForegroundColor $CommandInfo

# Getting context
$Context = Get-AzContext
if (-Not $Context)
{
    Write-Error "Can't get Az context! Not logged in?" -ErrorAction Continue
    Exit 1
}

# Checking KeyVault ressource group
Write-Host "Checking KeyVault ressource group for keyvault" -ForegroundColor $CommandInfo
$ResGrpKeyVault = Get-AzResourceGroup -Name $KeyVaultResourceGroupName -ErrorAction SilentlyContinue
if (-Not $ResGrpKeyVault)
{
    throw "KeyVault Ressource Group not found. Please create the Ressource Group $KeyVaultResourceGroupName"
}

# Checking key vault
Write-Host "Checking key vault" -ForegroundColor $CommandInfo
$KeyVault = Get-AzKeyVault -ResourceGroupName $KeyVaultResourceGroupName -VaultName $KeyVaultName -ErrorAction SilentlyContinue
if (-Not $KeyVault)
{
    throw "Key Vault not found. Please create the Key Vault $KeyVaultName"
}

# Setting own key vault access
Write-Host "Setting own key vault access" -ForegroundColor $CommandInfo
$user = Get-AzAdUser -UserPrincipalName $Context.Account.Id
if ($KeyVault.EnableRbacAuthorization)
{
    $RoleAssignment = Get-AzRoleAssignment -RoleDefinitionName "Key Vault Administrator" -ObjectId $user.Id -Scope $KeyVault.ResourceId -ErrorAction SilentlyContinue | Where-Object { $_.Scope -eq $KeyVault.ResourceId }
    $Retries = 0;
    While ($null -eq $RoleAssignment -and $Retries -le 6)
    {
        $RoleAssignment = New-AzRoleAssignment -RoleDefinitionName "Key Vault Administrator" -ObjectId $user.Id -Scope $KeyVault.ResourceId -ErrorAction SilentlyContinue
        Start-Sleep -s 10
        $RoleAssignment = Get-AzRoleAssignment -RoleDefinitionName "Key Vault Administrator" -ObjectId $user.Id -Scope $KeyVault.ResourceId -ErrorAction SilentlyContinue | Where-Object { $_.Scope -eq $KeyVault.ResourceId }
        $Retries++;
    }
    if ($Retries -gt 6)
    {
        throw "Was not able to set role assigment 'Key Vault Administrator' for user $($user.Id) on scope $($KeyVault.ResourceId)"
    }
}
else
{
    Set-AzKeyVaultAccessPolicy -VaultName $KeyVaultName -ObjectId $user.Id -PermissionsToCertificates "All" -PermissionsToSecrets "All" -PermissionsToKeys "All" -PermissionsToStorage "All" -ErrorAction Continue
}

# Checking azure key vault key
Write-Host "Checking azure key vault key" -ForegroundColor $CommandInfo
$CompName = Make-PascalCase($AlyaCompanyNameShort)
$CredentialAssetName = "$($CompName)AipServiceUserCredential"
$UserName = "$($CompName)Aip.ServiceUser"
$CredentialUserName = "$($UserName)@$($AlyaTenantName)"
$AzureKeyVaultSecret = Get-AzKeyVaultSecret -VaultName $KeyVaultName -Name $CredentialAssetName -ErrorAction SilentlyContinue
if (-Not $AzureKeyVaultKey)
{
    Write-Warning "Key Vault secret not found. Creating the secret $CredentialAssetName"
    $Password = "$" + [Guid]::NewGuid().ToString() + "!"
    $PasswordSec = ConvertTo-SecureString $Password -AsPlainText -Force
    Clear-Variable -Name Password -ErrorAction SilentlyContinue
    $AzureKeyVaultSecret = Set-AzKeyVaultSecret -VaultName $KeyVaultName -Name $CredentialAssetName -SecretValue $PasswordSec -ContentType $CredentialUserName
}
else
{
    Write-Warning "Key Vault secret found. Loading $CredentialAssetName"
    $AzureKeyVaultSecret = Get-AzKeyVaultSecret -VaultName $KeyVaultName -Name $CredentialAssetName
    $PasswordSec = $AzureKeyVaultSecret.SecretValue
}

# Checking Aip service account
Write-Host "Checking Aip service account" -ForegroundColor $CommandInfo
$User = Get-AzAdUser -UserPrincipalName $CredentialUserName -ErrorAction SilentlyContinue
if (-Not $User)
{
    Write-Warning "Aip service account not found. Creating the Aip service account $CredentialUserName"
    $null = New-AzADUser -DisplayName "$($CompName) Aip Service User" -MailNickname $UserName -UserPrincipalName $CredentialUserName -Password $PasswordSec -AccountEnabled $true -ShowInAddressList:$false -UsageLocation $AlyaDefaultUsageLocation -ForceChangePasswordNextLogin:$false -GivenName "$($CompName) Aip" -Surname "Service User"
    $User = Get-AzADUser -UserPrincipalName $CredentialUserName
}

# Checking password never expires
Write-Host "Checking password never expires" -ForegroundColor $CommandInfo
Set-AzADUser -UserPrincipalName $CredentialUserName -PasswordPolicy "DisablePasswordExpiration"

# Checking users MFA status
Write-Host "Checking users MFA status" -ForegroundColor $CommandInfo
$HasEntraIdP1Enabled = $AlyaLicenseType -notin @("BusinessBasic","BusinessStandard","EnterpriseOE1","EnterpriseOE3","EnterpriseOE5","EnterpriseME3orOE3EMSorA3")
if (-Not $HasEntraIdP1Enabled)
{
    Write-Warning "Your license plan $AlyaLicenseType does not support Conditional Access."
    Write-Warning "Please disable now MFA for user $CredentialUserName."
    pause
}
else
{

    # Getting group
    Write-Host "Getting group $AlyaMfaDisabledGroupName" -ForegroundColor $CommandInfo
    $noMfaGrp = Get-AzADGroup -DisplayName $AlyaMfaDisabledGroupName
    if (-Not $noMfaGrp)
    {
        throw "Can't find Group '$AlyaMfaDisabledGroupName'"
    }

    # Assigning user to group
    Write-Host "Assigning user to group" -ForegroundColor $CommandInfo
    $member = Get-AzADGroupMember -GroupObjectId $noMfaGrp.Id | Where-Object { $_.UserPrincipalName -eq $CredentialUserName }
    if (-Not $member)
    {
        Write-Warning "User is not yet a member. Adding it now."
        Add-AzADGroupMember -TargetGroupObjectId $noMfaGrp.Id -MemberObjectId $User.Id
    }

}

# Checking Aip service account role membership
Write-Host "Checking Aip service account role membership" -ForegroundColor $CommandInfo
$builtInRole = Get-MgBetaRoleManagementDirectoryRoleDefinition -Filter "displayName eq 'Azure Information Protection Administrator'"
$actMembs = Get-MgBetaRoleManagementDirectoryRoleAssignment -Filter "roleDefinitionId eq '$($builtInRole.Id)'" -All -ExpandProperty Principal
$actMemb = $actMembs | Where-Object { $_.PrincipalId -eq $User.Id}
if (-Not $actMemb)
{
    Write-Warning "Service account role membership not found. Creating the service account role membership $($builtInRole.DisplayName)"
    $otype = $User.GetType().Name.Replace("MicrosoftGraph", "")
    $oname = $User.UserPrincipalName
    if ([string]::IsNullOrEmpty($oname)) { $oname = $User.DisplayName }
    Write-Host "    adding $otype $oname $($User.Id)" -ForegroundColor $CommandWarning
    $actMemb = New-MgBetaRoleManagementDirectoryRoleAssignment -RoleDefinitionId $builtInRole.Id -PrincipalId $User.Id -DirectoryScopeId "/"
}

#Stopping Transscript
Stop-Transcript

