<#
.SYNOPSIS
Creates and configures an Azure Virtual Machine to serve as an AVD Image Host Server.

.DESCRIPTION
The Create-ImageHostServer.ps1 script automates the setup of an Azure Virtual Machine configured as an Image Host for Azure Virtual Desktop (AVD). It verifies and creates required Azure resources including resource groups, network interfaces, subnets, virtual networks, diagnostic storage, key vaults, and credentials. The script ensures that all dependencies are correctly configured before deploying and tagging the VM. It also manages the creation of secrets within an Azure Key Vault for administrative credentials and configures the VM with a specific image and size suitable for hosting AVD images.

.INPUTS
None. All configurations are provided through the environment configuration file loaded at runtime.

.OUTPUTS
Creates and configures an Azure Virtual Machine and required supporting Azure resources for image hosting.

.EXAMPLE
PS> .\Create-ImageHostServer.ps1

