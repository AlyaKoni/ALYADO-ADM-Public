Function Get-PerformanceCounterLocalName
{
    param
    (
        [UInt32]
        $ID,
        $ComputerName = $env:COMPUTERNAME
    )
    $code = '[DllImport("pdh.dll", SetLastError=true, CharSet=CharSet.Unicode)] public static extern UInt32 PdhLookupPerfNameByIndex(string szMachineName, uint dwNameIndex, System.Text.StringBuilder szNameBuffer, ref uint pcchNameBufferSize);'
    $Buffer = New-Object System.Text.StringBuilder(1024)
    [UInt32]$BufferSize = $Buffer.Capacity
    $t = Add-Type -MemberDefinition $code -PassThru -Name PerfCounter -Namespace Utility
    $rv = $t::PdhLookupPerfNameByIndex($ComputerName, $id, $Buffer, [Ref]$BufferSize)
    if ($rv -eq 0)
    {
        $Buffer.ToString().Substring(0, $BufferSize-1)
    }
    else
    {
        Throw 'Get-PerformanceCounterLocalName : Unable to retrieve localized name. Check computer name and performance counter ID.'
    }
}

function Get-PerformanceCounterID
{
    param
    (
        [Parameter(Mandatory=$true)]
        $Name
    )
    if ($script:perfHash -eq $null)
    {
        $key = 'Registry::HKEY_LOCAL_MACHINE\SOFTWARE\Microsoft\Windows NT\CurrentVersion\Perflib\CurrentLanguage'
        $counters = (Get-ItemProperty -Path $key -Name Counter).Counter
        $script:perfHash = @{}
        $all = $counters.Count
        for($i = 0; $i -lt $all; $i+=2)
        {
           $script:perfHash.$($counters[$i+1]) = $counters[$i]
        }
    }
    $script:perfHash.$Name
}

$counterSets = (Get-Counter -ListSet *).countersetname

$winSystemLocale = Get-WinSystemLocale
$counters = @{}
if (Test-Path "$($AlyaData)\client\PerformanceCounters.xml")
{
    $counters = Import-Clixml -Path "$($AlyaData)\client\PerformanceCounters.xml"
}
if (-Not $counters[$winSystemLocale.LCID])
{
    $counters[$winSystemLocale.LCID] = @{}
}
foreach($counterSet in $counterSets)
{
    Write-Host "`nCounter Set: $counterSet"
    $setId = Get-PerformanceCounterID -Name $counterSet
    if (-Not $setId -or $setId -eq 0)
    {
        Write-Host "Warning: Counter Set ID not found for Counter Set: $counterSet"
        $setId = Get-Random -Minimum 100000 -Maximum 999999
    }
    if (-Not $counters[$winSystemLocale.LCID][$setId])
    {
        $counters[$winSystemLocale.LCID][$setId] = @{
            Name = $counterSet
            DisplayName = $null
            Counters = @{}
        }
    }
    $countersInSet = Get-Counter -ListSet $counterSet | Select-Object -ExpandProperty Counter
    foreach($counter in $countersInSet)
    {
        Write-Host "  Counter: $counter"
        $counterKeys = $counter.Split("\", [System.StringSplitOptions]::RemoveEmptyEntries)
        $counterSetName = $counterKeys[0]
        if ($counters[$winSystemLocale.LCID][$setId].DisplayName -ne $counterSetName)
        {
            if ($counters[$winSystemLocale.LCID][$setId].DisplayName)
            {
                Write-Host "Warning: Counter Set Name mismatch! Existing: $($counters[$winSystemLocale.LCID][$setId].DisplayName), New: $counterSetName"
            }
            $counters[$winSystemLocale.LCID][$setId].DisplayName = $counterSetName
        }
        $counterName = $counterKeys[1]
        $counterId = Get-PerformanceCounterID -Name $counterName
        if (-Not $counterId -or $counterId -eq 0)
        {
            Write-Host "Warning: Counter ID not found for Counter: $counterName"
            $counterId = Get-Random -Minimum 100000 -Maximum 999999
        }
        $counters[$winSystemLocale.LCID][$setId].Counters[$counterId] = @{
            Name = $counterName
            FullName = $counter
        }
    }
}
$counters | Export-Clixml -Path "$($AlyaData)\client\PerformanceCounters.xml" -Force

