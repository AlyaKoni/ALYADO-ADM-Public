<#
.SYNOPSIS
Generates and updates a list of Windows performance counters and their IDs, exporting the data to an XML file for localized reference.

.DESCRIPTION
The Get-PerformanceCounters.ps1 script retrieves all available performance counter sets and their associated counters from the local system. It uses internal helper functions to resolve performance counter IDs and localized names, compares them with any existing data stored in PerformanceCounters.xml, and updates the file accordingly. The script supports localization by storing data per system locale and logs all actions during execution.

.INPUTS
None. The script does not require input objects.

.OUTPUTS
None. The script outputs logs and updates an XML file containing performance counter definitions.

.EXAMPLE
PS> .\Get-PerformanceCounters.ps1

