<#
.SYNOPSIS
Enables and configures BitLocker encryption on a Windows system drive, ensuring compliance with organizational requirements and backing up recovery keys to Active Directory.

.DESCRIPTION
The Enable-BitLocker.ps1 script automates the process of configuring, enabling, and managing BitLocker drive encryption on Windows systems. It performs prerequisite checks, initializes the TPM if required, configures registry policies for Active Directory key backup, partitions drives as necessary, and enables BitLocker protection. The script only executes on domain-joined systems and ensures recovery passwords are stored securely in Active Directory. It also creates detailed log files for auditing and troubleshooting.

.INPUTS
None. The script does not accept input through the pipeline or parameters.

.OUTPUTS
Transcript log file containing the results of BitLocker configuration and enablement.
Console output providing status and error information.

.EXAMPLE
PS> .\Enable-BitLocker.ps1
Executes the script to automatically configure and enable BitLocker encryption on the system drive of a domain-joined computer, backing up the recovery key to Active Directory.

