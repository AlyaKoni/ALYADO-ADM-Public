<#
.SYNOPSIS
Restores user-specific data and settings from a previously saved configuration backup.

.DESCRIPTION
The RestoreUserData.ps1 script restores user data and application settings for the current Windows user from a backup directory structure. It attempts to recover various user-specific components such as MRU registry entries, taskbar shortcuts, Outlook signatures, and configuration files for popular browsers (Firefox, Edge, Chrome), as well as personal folders (Downloads, Music, Videos, Desktop, Documents, and Pictures). The script automatically determines the user and host names, handles missing backups, and logs actions via a transcript file.

.INPUTS
None. The script uses the current user's environment variables and a predefined backup directory structure.

.OUTPUTS
A transcript log file containing the results of the data restoration process, along with console output indicating progress and status of each restored component.

.EXAMPLE
PS> .\RestoreUserData.ps1
Executes the script to restore the current user's application data, browser settings, and personal folders from the backup structure located in the script directory.

