<#
.SYNOPSIS
Creates encrypted Intune Mac application packages from local sources, downloading installers if required, and preparing metadata for deployment.

.DESCRIPTION
The Create-IntuneMACPackages.ps1 script automates the process of building encrypted Intune packages for macOS applications. It scans a specified directory for application folders, optionally filters by name, cleans and rebuilds the content and package directories, executes helper scripts for preparation and versioning, downloads installer files, extracts and prepares the package, encrypts the final installer, and generates the associated JSON file for Intune deployment. The script supports resuming from a specific app and reusing existing packages to optimize the workflow.

.PARAMETER CreateOnlyAppWithName
Specifies a single application name to process, ignoring all others in the source directory.

.PARAMETER ContinueAtAppWithName
Specifies an application name at which to resume processing, skipping all prior apps.

.PARAMETER AppsPath
Defines the relative path (inside the Intune data root) where application package folders are located. Default is "MACApps".

.PARAMETER ReuseExistingPackages
When set to $true, existing package files are reused without rebuilding them. Default is $false.

.INPUTS
None. The script does not accept input from the pipeline.

.OUTPUTS
Creates encrypted macOS Intune package files and associated JSON metadata within each application’s package directory. Logs are written to the AlyaScripts logging path.

.EXAMPLE
PS> .\Create-IntuneMACPackages.ps1 -AppsPath "MACApps" -ReuseExistingPackages $false

.NOTES
