#Reading configuration
. $PSScriptRoot\..\..\01_ConfigureEnv.ps1

#Starting Transscript
Start-Transcript -Path "$($AlyaLogs)\scripts\security\Enable-PIMforAllAdmins-$($AlyaTimeString).log" | Out-Null

# Checking modules
Write-Host "Checking modules" -ForegroundColor $CommandInfo
Install-ModuleIfNotInstalled "Microsoft.Graph.Authentication"
Install-ModuleIfNotInstalled "Microsoft.Graph.Beta.Groups"
Install-ModuleIfNotInstalled "Microsoft.Graph.Beta.Users"
Install-ModuleIfNotInstalled "Microsoft.Graph.Beta.Identity.SignIns"
Install-ModuleIfNotInstalled "Microsoft.Graph.Beta.Identity.Governance"

# Logging in
Write-Host "Logging in" -ForegroundColor $CommandInfo
LoginTo-MgGraph -Scopes "Directory.ReadWrite.All","RoleManagement.Read.All","RoleManagement.ReadWrite.Directory"

# =============================================================
# Azure stuff
# =============================================================

Write-Host "`n`n=====================================================" -ForegroundColor $CommandInfo
Write-Host "PIM | Enable-PIMforAllAdmins | Graph" -ForegroundColor $CommandInfo
Write-Host "=====================================================`n" -ForegroundColor $CommandInfo

Write-Host "Checking  license" -ForegroundColor $CommandInfo
try
{
    $actMembs = Get-MgBetaRoleManagementDirectoryRoleEligibilitySchedule -All -ExpandProperty Principal
}
catch
{
    if ($_.Exception.ToString() -like "*AadPremiumLicenseRequired*" -or $_.Exception.ToString() -like "*AAD Premium 2*")
    {
        Write-Host "No  license available! Can't configure PIM roles."
        $configurePIM = $false
    }
    else
    {
        throw $_.Exception
    }
}

# Getting special handled objects
Write-Host "Getting special handled objects" -ForegroundColor $CommandInfo
$CustomerLockboxApproverGroup = $null
if (-Not [string]::IsNullOrEmpty($AlyaCustomerLockboxApproverGroupName))
{
    $CustomerLockboxApproverGroup = Get-MgBetaGroup -Filter "displayName eq '$AlyaCustomerLockboxApproverGroupName'" -ErrorAction SilentlyContinue
    if (-not $CustomerLockboxApproverGroup)
    {
        throw "Customer Lockbox Approver Group $AlyaCustomerLockboxApproverGroupName not found!"
    }
}
$DeviceAdminsGroup = $null
if (-Not [string]::IsNullOrEmpty($AlyaDeviceAdminsGroupName))
{
    $DeviceAdminsGroup = Get-MgBetaGroup -Filter "displayName eq '$AlyaDeviceAdminsGroupName'" -ErrorAction SilentlyContinue
    if (-not $DeviceAdminsGroup)
    {
        throw "Alya Device Admins Group $AlyaDeviceAdminsGroupName not found!"
    }
}
$CompName = Make-PascalCase($AlyaCompanyNameShort)
$UserName = "$($CompName)Aip.ServiceUser"
$UserPrincipalName = "$($UserName)@$($AlyaTenantName)"
$LockBoxServiceUser = Get-MgBetaUser -UserId $UserPrincipalName -ErrorAction SilentlyContinue
$BreakGlassAdminUser = Get-MgBetaUser -UserId $AlyaBreakingGlassUserName -ErrorAction SilentlyContinue
if (-not $BreakGlassAdminUser)
{
    throw "Alya Break Glass user $AlyaBreakingGlassUserName not found!"
}

# Getting all built in roles
Write-Host "Getting all built in roles" -ForegroundColor $CommandInfo
$roleDefinitions = Get-MgBetaRoleManagementDirectoryRoleDefinition -All

# Getting  permanent role members
Write-Host "Getting  permanent role members" -ForegroundColor $CommandInfo
$assignedRoles = Get-MgBetaRoleManagementDirectoryRoleAssignment -All -ExpandProperty Principal
$permanetAssignments = @()
$allPermanetAssignments = @()
foreach ($roleDefinition in $roleDefinitions)
{
    $actMembs = $assignedRoles | Where-Object { $_.RoleDefinitionId -eq $roleDefinition.Id }
    if ($actMembs -and $actMembs.Count -gt 0)
    {
        Write-Host "Role: $($roleDefinition.DisplayName)"
        foreach ($actMemb in $actMembs)
        {
            $isUser = $true
            switch ($actMemb.Principal.AdditionalProperties.'@odata.type')
            {
                "#Microsoft.Graph.user" {
                }
                "#Microsoft.Graph.servicePrincipal" {
                    $isUser = $false
                }
                "#Microsoft.Graph.group" {
                    $isUser = $false
                }
                default {
                    throw "Unknown principal type: $($actMemb.Principal.AdditionalProperties.'@odata.type')"
                }
            }
            if ($isUser)
            {
                if ($RolesToEnablePIM -contains $roleDefinition.DisplayName -and @($CustomerLockboxApproverGroup.Id, $DeviceAdminsGroup.Id, $LockBoxServiceUser.Id, $BreakGlassAdminUser.Id) -notcontains $actMemb.Principal.Id)
                {
                    $permanetAssignments += @{
                        RoleDefinition = $roleDefinition
                        Member = $actMemb
                    }
                }
                $allPermanetAssignments += @{
                    RoleDefinition = $roleDefinition
                    Member = $actMemb
                }
            }
        }
    }
}

# Getting eligable role members
Write-Host "Getting eligable role members" -ForegroundColor $CommandInfo
$assignedRoles = @()
try {
    $assignedRoles = Get-MgBetaRoleManagementDirectoryRoleEligibilitySchedule -All -ExpandProperty Principal
}
catch {}
$eligableAssignments = @()
foreach ($roleDefinition in $roleDefinitions)
{
    if (-not ($RolesToEnablePIM -contains $roleDefinition.DisplayName))
    {
        continue
    }
    $actMembs = $assignedRoles | Where-Object { $_.RoleDefinitionId -eq $roleDefinition.Id }
    if ($actMembs -and $actMembs.Count -gt 0)
    {
        Write-Host "Role: $($roleDefinition.DisplayName)"
        foreach ($actMemb in $actMembs)
        {
            $isUser = $true
            switch ($actMemb.Principal.AdditionalProperties.'@odata.type')
            {
                "#Microsoft.Graph.user" {
                }
                "#Microsoft.Graph.servicePrincipal" {
                    $isUser = $false
                }
                "#Microsoft.Graph.group" {
                    $isUser = $false
                }
                default {
                    throw "Unknown principal type: $($actMemb.Principal.AdditionalProperties.'@odata.type')"
                }
            }
            if ($isUser)
            {
                $eligableAssignments += @{
                    RoleDefinition = $roleDefinition
                    Member = $actMemb
                }
            }
        }
    }
}

# Configuring eligable role members
Write-Host "Configuring eligable role members" -ForegroundColor $CommandInfo
$privRole = $roleDefinitions | Where-Object { $_.DisplayName -eq "Privileged Role Administrator" }
foreach($permanetAssignment in $permanetAssignments)
{
    $alreadyEligable = $false
    if ($permanetAssignment.Member.Principal.Id -eq $BreakGlassAdminUser.Id -and
        $permanetAssignment.RoleDefinition.Id -ne $privRole.Id)
    {
        Write-Warning "Skipping removing permanent role $($permanetAssignment.RoleDefinition.DisplayName) from break glass user $($BreakGlassAdminUser.UserPrincipalName)"
        continue
    }
    foreach($eligableAssignment in $eligableAssignments)
    {
        if ($permanetAssignment.Member.Principal.Id -eq $eligableAssignment.Member.Principal.Id -and
            $permanetAssignment.RoleDefinition.Id -eq $eligableAssignment.RoleDefinition.Id)
        {
            $alreadyEligable = $true
            break
        }
    }
    if (-not $alreadyEligable)
    {
        $principal = Get-MgBetaUser -UserId $permanetAssignment.Member.Principal.Id
        $json = @"
{
    "principalId": "$($permanetAssignment.Member.Principal.Id)",
    "roleDefinitionId": "$($permanetAssignment.RoleDefinition.Id)",
    "justification": "Added eligible assignment from Alya PowerShell",
    "directoryScopeId": "/",
    "action": "adminAssign",
    "scheduleInfo": {
        "startDateTime": "$((Get-Date).Date.ToString("o"))",
        "expiration": {}
    }
}
"@
        try {
            Write-Host "Enabling PIM for user $($principal.UserPrincipalName) for role $($permanetAssignment.RoleDefinition.DisplayName)"
            New-MgBetaRoleManagementDirectoryRoleEligibilityScheduleRequest -AdditionalProperties ($json | ConvertFrom-Json -AsHashtable -Depth 99) | Out-Null
            Write-Host "Removing permanent role $($permanetAssignment.RoleDefinition.DisplayName) from user $($principal.UserPrincipalName)"
            if ((Get-MgContext).Account -eq $principal.UserPrincipalName)
            {
                Write-Warning "You can't remove yourself! Please use break glass $AlyaBreakingGlassUserName to remove the permanent role assignment for yourself manually."
                pause
            }
            else
            {
                Remove-MgBetaRoleManagementDirectoryRoleAssignment -UnifiedRoleAssignmentId $permanetAssignment.Member.Id -Confirm:$false
            }
        }
        catch {
            Write-Error $_.Exception -ErrorAction Continue
        }
    }
}

#Stopping Transscript
Stop-Transcript

