<#
.SYNOPSIS
Enables Privileged Identity Management (PIM) for all specified administrative roles in Azure AD, converting permanent assignments into eligible assignments.

.DESCRIPTION
The Enable-PIMforAllAdmins.ps1 script connects to Microsoft Graph using the Microsoft.Graph PowerShell modules, retrieves all built-in and assigned directory roles, and enables PIM for users assigned to selected administrative roles. It skips certain special accounts like the Break Glass user, Lockbox service user, and predefined administrator groups. The script verifies required licenses, modules, and configurations, and transitions permanent role assignments into eligible roles managed through Azure AD PIM.

.PARAMETER RolesToEnablePIM
Specifies the list of Azure AD administrative roles for which PIM should be enabled. By default, the script includes a predefined set of high-privilege roles such as Global Administrator, Privileged Role Administrator, and others.

.INPUTS
None. Configuration values and parameters are provided directly or through environment configuration scripts.

.OUTPUTS
Produces log files detailing operations and results of the PIM configuration process. Outputs are written to the transcript log file in the Alya logging directory.

.EXAMPLE
PS> .\Enable-PIMforAllAdmins.ps1
Runs the script using the default list of administrative roles, enabling PIM for all applicable users and converting their permanent assignments to eligible assignments.

